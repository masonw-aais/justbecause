// Helper function to get flowers by category
export const getFlowersByCategory = (
  categoryId: string
): IndividualFlower[] => {
  return individualFlowers.filter((flower) => {
    const flowerType = flower.id.split("-")[0];
    return flowerType === categoryId;
  });
};

// Helper function to get category by ID
export const getCategoryById = (
  categoryId: string
): FlowerCategory | undefined => {
  return flowerCategories.find((category) => category.id === categoryId);
};
